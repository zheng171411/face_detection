package com.zdw.faceBean;

public class Blur {
	ThValue blurness;
	ThValue motionblur;
	ThValue gaussianblur;
	public ThValue getBlurness() {
		return blurness;
	}
	public void setBlurness(ThValue blurness) {
		this.blurness = blurness;
	}
	public ThValue getMotionblur() {
		return motionblur;
	}
	public void setMotionblur(ThValue motionblur) {
		this.motionblur = motionblur;
	}
	public ThValue getGaussianblur() {
		return gaussianblur;
	}
	public void setGaussianblur(ThValue gaussianblur) {
		this.gaussianblur = gaussianblur;
	}
}
